    public void supplierLoad(String colum, String type, String search) {
        
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `supplier`"
                    + " INNER JOIN `company` ON `supplier`.`company_id` = `company`.`id` WHERE `mobile` LIKE '" + search + "%' ORDER BY `" + colum + "`" + type + "");
            
            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            table.setRowCount(0);
            
            while (resultset.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultset.getString("mobile"));
                vector.add(resultset.getString("first_name"));
                vector.add(resultset.getString("last_name"));
                vector.add(resultset.getString("email"));
                vector.add(resultset.getString("company.name"));
                
                table.addRow(vector);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
